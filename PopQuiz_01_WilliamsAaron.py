#This program will calculate the average of two numbers

#Place where the user inputs their numbers
firstNumber = float(input('Please type in the first number: '))
secondNumber = float(input('Please type in the second number: '))

#Calculations to find the average
sumOfNumbers = firstNumber + secondNumber
average = sumOfNumbers / 2

#Displays the results
print('The average of these two numbers will be:',average)
